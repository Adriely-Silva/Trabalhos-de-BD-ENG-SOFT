class Contato:
    
    def __init__(self, id, nome, telefone, endereco, email, cidade, cep, id_agenda):
        self.id = id
        self.nome = nome
        self.telefone = telefone
        self.endereco = endereco
        self.email = email
        self.cidade = cidade
        self.cep = cep
        self.id_agenda = id_agenda